def tr_metric(language: str, label: str) -> str:
    if language == "es":
        return label
    canonical = _canonical_metric_label(label)
    for source_label, translations in METRIC_LABELS.items():
        if _canonical_metric_label(source_label) == canonical:
            return translations.get(language, label)
    return label


def _canonical_metric_label(label: str) -> str:
    repaired = _repair_mojibake(label)
    normalized = unicodedata.normalize("NFKD", repaired).encode("ascii", "ignore").decode("ascii")
    return normalized.strip().lower()


def _repair_mojibake(text: str) -> str:
    try:
        repaired = text.encode("latin1").decode("utf-8")
        if repaired:
            return repaired
    except Exception:
        pass
    return text
